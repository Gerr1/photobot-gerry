import "./bootstrap";
import "../css/app.css";

import { createRoot } from "react-dom/client";
import { createInertiaApp } from "@inertiajs/react";
import { resolvePageComponent } from "laravel-vite-plugin/inertia-helpers";

// UBAH DISINI: Ganti 'Laravel' jadi 'PhotoBot'
const appName = import.meta.env.VITE_APP_NAME || "PhotoBot";

createInertiaApp({
    title: (title) => `${title} - ${appName}`,
    resolve: (name) =>
        resolvePageComponent(
            `./Pages/${name}.jsx`,
            import.meta.glob("./Pages/**/*.jsx")
        ),
    setup({ el, App, props }) {
        const root = createRoot(el);

        root.render(<App {...props} />);
    },
    progress: {
        color: "#EC4899", // Saya ganti jadi Pink (sesuai tema PhotoBot kamu), defaultnya abu-abu
    },
});
